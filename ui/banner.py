        cucumber = Text.from_ansi(_CUCUMBER_ASCII)

        # ---- 右栏：应用信息 ----
        info = Text()
        info.append("gurkecode", style="bold")
        info.append(f" v{APP_VERSION}\n黄瓜🥒编码\n")
        info.append("工作目录：", style="dim")
        info.append(cwd, style="dim")

        # ---- 横向两栏布局 ----
        super().__init__(Columns([cucumber, info]))
